//
// DEFINE THE DATABASE
// COLUMN 1: LOCAL TIME STAMP, WHEN THE URL WAS ADDED TO LOCAL DATABASE
// THE TIME STAMP IS ONLY LOCAL, AND NOT TRANSFERRED TO THE REMOTE PEER
// COLUMN 2: TITLE OR DESCRIPTION OF THE URL
// COLUMN 3: KEYWORDS UP TO 160 DIGITS, SEPERATED BY BLANKS
