// A PEERS STARTING THEN IS STARTING AT A GIVEN NUMBER OF URL AND COUNTING DOWN
// AS LONG AS TIME IS GIVEN UNTILL THE IP DROP AND CONNECTION CUT HAPPENS
// NEW ADDED URLS WHILE THIS PROCESS IS DONE FOR 30 PEERS CAN BE ADDED WITH 
// HIGHER NUMBERS, SO THAT THEY WILL NOT APPEAR IN THE TRANSFER OF THE CURRENT PEER
// CONNECTION; AS THIS IS COUNTING NUMBERS DOWN; NEW PEERS ADD NEW URLS NUMBERS UP.
