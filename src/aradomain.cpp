//
// CHECK AT STARTUP, IF THERE IS A NODECACHE.TXT FILE 
// NEXT TO THE EXE, IF NOT, CREATE SUCH A FILE
//


// 
// COUNT THE AMOUNT OF THE NODECACHE
// DEFINE VARIABLE IP CACHE, DEFAULT 1500
